class StaticDefaultValuesPrint{
           static Byte _size ;    
           static short _short;
           static int _int;
           static long _long;
           static double _double ;
           static char _character;
           static boolean _IsTrue ;
           static float _float;   


           public static void main(String [] args)
             {
            System.out.println("print the default value");
             System.out.println(_size);

             System.out.println(_short);

             System.out.println( _int );

             System.out.println( _long );

             System.out.println( _double);
             
             System.out.println(_character);
          
             System.out.println(_IsTrue); 
             
             System.out.println(_float); 
             
          }
}